    private var cacheVersion: [UUID: Int] = [:]             // ìŠ¤ëƒ…ìƒ· ë²„ì „ ê´€ë¦¬
    
    // MARK: - íŒŒì¼ ì‹œìŠ¤í…œ ê²½ë¡œ
    private var bfCacheDirectory: URL {
        let documentsPath = FileManager.default.urls(for: .documentDirectory, in: .userDomainMask).first!
        return documentsPath.appendingPathComponent("BFCache", isDirectory: true)
    }
    
    private func tabDirectory(for tabID: UUID) -> URL {
        return bfCacheDirectory.appendingPathComponent("Tab_\(tabID.uuidString)", isDirectory: true)
    }
    
    private func pageDirectory(for pageID: UUID, tabID: UUID, version: Int) -> URL {
        return tabDirectory(for: tabID).appendingPathComponent("Page_\(pageID.uuidString)_v\(version)", isDirectory: true)
    }
    
    // MARK: - ì „í™˜ ìƒíƒœ
    private var activeTransitions: [UUID: TransitionContext] = [:]
    
    // ì „í™˜ ì»¨í…ìŠ¤íŠ¸
    private struct TransitionContext {
        let tabID: UUID
        weak var webView: WKWebView?
        weak var stateModel: WebViewStateModel?
        var isGesture: Bool
        var direction: NavigationDirection
        var initialTransform: CGAffineTransform
        var previewContainer: UIView?
        var currentSnapshot: UIImage?
    }
    
    enum NavigationDirection {
        case back, forward
    }
    
    enum CaptureType {
        case immediate  // í˜„ì¬ í˜ì´ì§€ (ë†’ì€ ìš°ì„ ìˆœìœ„)
        case background // ê³¼ê±° í˜ì´ì§€ (ì¼ë°˜ ìš°ì„ ìˆœìœ„)
    }
    
    // MARK: - ğŸ’¾ ë””ìŠ¤í¬ ìºì‹œ ê´€ë¦¬
    
    private func createDirectoryIfNeeded(at url: URL) {
        if !FileManager.default.fileExists(atPath: url.path) {
            try? FileManager.default.createDirectory(at: url, withIntermediateDirectories: true, attributes: nil)
        }
    }
    
    private func loadDiskCacheIndex() {
        diskIOQueue.async { [weak self] in
            guard let self = self else { return }
            
            // BFCache ë””ë ‰í† ë¦¬ ìƒì„±
            self.createDirectoryIfNeeded(at: self.bfCacheDirectory)
            
            // ëª¨ë“  íƒ­ ë””ë ‰í† ë¦¬ ìŠ¤ìº”
            do {
                let tabDirs = try FileManager.default.contentsOfDirectory(at: self.bfCacheDirectory, includingPropertiesForKeys: nil, options: .skipsHiddenFiles)
                
                for tabDir in tabDirs {
                    if tabDir.lastPathComponent.hasPrefix("Tab_") {
                        // ê° í˜ì´ì§€ ë””ë ‰í† ë¦¬ ìŠ¤ìº”
                        let pageDirs = try FileManager.default.contentsOfDirectory(at: tabDir, includingPropertiesForKeys: nil, options: .skipsHiddenFiles)
                        
                        for pageDir in pageDirs {
                            if pageDir.lastPathComponent.hasPrefix("Page_") {
                                // metadata.json ë¡œë“œ
                                let metadataPath = pageDir.appendingPathComponent("metadata.json")
                                if let data = try? Data(contentsOf: metadataPath),
                                   let metadata = try? JSONDecoder().decode(CacheMetadata.self, from: data) {
                                    
                                    self.storageQueue.async(flags: .barrier) {
                                        self.diskCacheIndex[metadata.pageID] = pageDir.path
                                        self.cacheVersion[metadata.pageID] = metadata.version
                                    }
                                }
                            }
                        }
                    }
                }
                
                self.dbg("ğŸ’¾ ë””ìŠ¤í¬ ìºì‹œ ì¸ë±ìŠ¤ ë¡œë“œ ì™„ë£Œ: \(self.diskCacheIndex.count)ê°œ í•­ëª©")
            } catch {
                self.dbg("âŒ ë””ìŠ¤í¬ ìºì‹œ ë¡œë“œ ì‹¤íŒ¨: \(error)")
            }
        }
    }
    
    private struct CacheMetadata: Codable {
        let pageID: UUID
        let tabID: UUID
        let version: Int
        let timestamp: Date
        let url: String
        let title: String
    }
    
    // MARK: - ğŸ“¸ ì§ë ¬í™”ëœ ìº¡ì²˜ ì‹œìŠ¤í…œ
    
    func captureSnapshot(pageRecord: PageRecord, webView: WKWebView?, type: CaptureType = .immediate, tabID: UUID? = nil) {
        guard let webView = webView else {
            dbg("âŒ ìº¡ì²˜ ì‹¤íŒ¨: ì›¹ë·° ì—†ìŒ - \(pageRecord.title)")
            return
        }
        
        // ìš°ì„ ìˆœìœ„ì— ë”°ë¥¸ í ì„ íƒ
        let queue = (type == .immediate) ? highPriorityCaptureQueue : normalCaptureQueue
        
        queue.async { [weak self] in
            self?.performSerialCapture(pageRecord: pageRecord, webView: webView, tabID: tabID)
        }
    }
    
    private func performSerialCapture(pageRecord: PageRecord, webView: WKWebView, tabID: UUID? = nil) {
        // ë©”ì¸ ìŠ¤ë ˆë“œì—ì„œ ì›¹ë·° ìƒíƒœ í™•ì¸
        let captureData = DispatchQueue.main.sync { () -> CaptureData? in
            // ì›¹ë·°ê°€ ì¤€ë¹„ë˜ì—ˆëŠ”ì§€ í™•ì¸
            guard webView.window != nil, !webView.bounds.isEmpty else {
                dbg("âš ï¸ ì›¹ë·° ì¤€ë¹„ ì•ˆë¨ - ìº¡ì²˜ ìŠ¤í‚µ: \(pageRecord.title)")
                return nil
            }
            
            return CaptureData(
                scrollPosition: webView.scrollView.contentOffset,
                bounds: webView.bounds,
                isLoading: webView.isLoading
            )
        }
        
        guard let data = captureData else { return }
        
        // ë¡œë”© ì¤‘ì´ë©´ ì ì‹œ ëŒ€ê¸°
        if data.isLoading {
            Thread.sleep(forTimeInterval: 0.2)
        }
        
        // ë™ê¸°ì‹ ìº¡ì²˜ ìˆ˜í–‰
        let captureResult = self.syncCapture(pageRecord: pageRecord, webView: webView, captureData: data)
        
        // ì´ë¯¸ì§€ë¥¼ ë””ìŠ¤í¬ì— ì €ì¥í•˜ê³  ê²½ë¡œë§Œ ìŠ¤ëƒ…ìƒ·ì— ì €ì¥
        if let tabID = tabID {
            self.saveToDisk(snapshot: captureResult, tabID: tabID)
        } else {
            // tabIDê°€ ì—†ìœ¼ë©´ ë©”ëª¨ë¦¬ì—ë§Œ ì €ì¥
            self.storeInMemory(captureResult.snapshot, for: pageRecord.id)
        }
    }
    
    private struct CaptureData {
        let scrollPosition: CGPoint
        let bounds: CGRect
        let isLoading: Bool
    }
    
    private func syncCapture(pageRecord: PageRecord, webView: WKWebView, captureData: CaptureData) -> (snapshot: BFCacheSnapshot, image: UIImage?) {
        var visualSnapshot: UIImage? = nil
        var domSnapshot: String? = nil
        var jsState: [String: Any]? = nil
        var formData: [String: Any]? = nil
        let semaphore = DispatchSemaphore(value: 0)
        
        // 1. ë¹„ì£¼ì–¼ ìŠ¤ëƒ…ìƒ· (ë©”ì¸ ìŠ¤ë ˆë“œ)
        DispatchQueue.main.sync {
            let config = WKSnapshotConfiguration()
            config.rect = captureData.bounds
            config.afterScreenUpdates = false
            
            webView.takeSnapshot(with: config) { image, error in
                if let error = error {
                    self.dbg("ğŸ“¸ ìŠ¤ëƒ…ìƒ· ì‹¤íŒ¨, fallback ì‚¬ìš©: \(error.localizedDescription)")
                    // Fallback: layer ë Œë”ë§
                    visualSnapshot = self.renderWebViewToImage(webView)
                } else {
                    visualSnapshot = image
                    self.dbg("ğŸ“¸ ìŠ¤ëƒ…ìƒ· ì„±ê³µ: \(pageRecord.title)")
                }
                semaphore.signal()
            }
        }
        semaphore.wait()
        
        // 2. DOM ìº¡ì²˜ (ê°„ì†Œí™”)
        DispatchQueue.main.sync {
            let domScript = """
            (function() {
                try {
                    if (document.readyState !== 'complete') return null;
                    const html = document.documentElement.outerHTML;
                    return html.length > 100000 ? html.substring(0, 100000) : html;
                } catch(e) { return null; }
            })()
            """
            
            webView.evaluateJavaScript(domScript) { result, error in
                domSnapshot = result as? String
                semaphore.signal()
            }
        }
        semaphore.wait()
        
        // 3. JS ìƒíƒœ ìº¡ì²˜ (ê°„ì†Œí™”)
        DispatchQueue.main.sync {
            let jsScript = """
            (function() {
                try {
                    const formData = {};
                    document.querySelectorAll('input:not([type="password"]), textarea, select').forEach((el, i) => {
                        if (i >= 50) return; // ìµœëŒ€ 50ê°œ
                        if (el.name || el.id) {
                            formData[el.name || el.id] = el.value || el.checked;
                        }
                    });
                    
                    return {
                        forms: formData,
                        scroll: { x: window.scrollX, y: window.scrollY },
                        href: window.location.href,
                        title: document.title
                    };
                } catch(e) { return null; }
            })()
            """
            
            webView.evaluateJavaScript(jsScript) { result, error in
                if let data = result as? [String: Any] {
                    jsState = data
                    formData = data["forms"] as? [String: Any]
                }
                semaphore.signal()
            }
        }
        semaphore.wait()
        
        // ìº¡ì²˜ ìƒíƒœ ê²°ì •
        let captureStatus: BFCacheSnapshot.CaptureStatus
        if visualSnapshot != nil && domSnapshot != nil && jsState != nil {
            captureStatus = .complete
        } else if visualSnapshot != nil {
            captureStatus = jsState != nil ? .partial : .visualOnly
        } else {
            captureStatus = .failed
        }
        
        // ë²„ì „ ì¦ê°€
        let version = (self.cacheVersion[pageRecord.id] ?? 0) + 1
        self.cacheVersion[pageRecord.id] = version
        
        let snapshot = BFCacheSnapshot(
            pageRecord: pageRecord,
            domSnapshot: domSnapshot,
            scrollPosition: captureData.scrollPosition,
            jsState: jsState,
            formData: formData,
            timestamp: Date(),
            webViewSnapshotPath: nil,  // ë‚˜ì¤‘ì— ë””ìŠ¤í¬ ì €ì¥ì‹œ ì„¤ì •
            captureStatus: captureStatus,
            version: version
        )
        
        dbg("âœ… ìº¡ì²˜ ì™„ë£Œ: \(pageRecord.title) [v\(version)] [ìƒíƒœ: \(captureStatus)]")
        return (snapshot, visualSnapshot)
    }
    
    private func renderWebViewToImage(_ webView: WKWebView) -> UIImage? {
        let renderer = UIGraphicsImageRenderer(bounds: webView.bounds)
        return renderer.image { context in
            webView.layer.render(in: context.cgContext)
        }
    }
    
    // MARK: - ğŸ’¾ ë””ìŠ¤í¬ ì €ì¥
    
    private func saveToDisk(snapshot: (snapshot: BFCacheSnapshot, image: UIImage?), tabID: UUID) {
        diskIOQueue.async { [weak self] in
            guard let self = self else { return }
            
            let pageID = snapshot.snapshot.pageRecord.id
            let version = snapshot.snapshot.version
            let pageDir = self.pageDirectory(for: pageID, tabID: tabID, version: version)
            
            // ë””ë ‰í† ë¦¬ ìƒì„±
            self.createDirectoryIfNeeded(at: pageDir)
            
            var finalSnapshot = snapshot.snapshot
            
            // 1. ì´ë¯¸ì§€ ì €ì¥ (JPEG ì••ì¶•)
            if let image = snapshot.image {
                let imagePath = pageDir.appendingPathComponent("snapshot.jpg")
                if let jpegData = image.jpegData(compressionQuality: 0.7) {
                    try? jpegData.write(to: imagePath)
                    finalSnapshot.webViewSnapshotPath = imagePath.path
                    self.dbg("ğŸ’¾ ì´ë¯¸ì§€ ì €ì¥: \(imagePath.lastPathComponent)")
                }
            }
            
            // 2. ìƒíƒœ ë°ì´í„° ì €ì¥ (JSON)
            let statePath = pageDir.appendingPathComponent("state.json")
            if let stateData = try? JSONEncoder().encode(finalSnapshot) {
                try? stateData.write(to: statePath)
                self.dbg("ğŸ’¾ ìƒíƒœ ì €ì¥: \(statePath.lastPathComponent)")
            }
            
            // 3. ë©”íƒ€ë°ì´í„° ì €ì¥
            let metadata = CacheMetadata(
                pageID: pageID,
                tabID: tabID,
                version: version,
                timestamp: Date(),
                url: snapshot.snapshot.pageRecord.url.absoluteString,
                title: snapshot.snapshot.pageRecord.title
            )
            
            let metadataPath = pageDir.appendingPathComponent("metadata.json")
            if let metadataData = try? JSONEncoder().encode(metadata) {
                try? metadataData.write(to: metadataPath)
            }
            
            // 4. ì¸ë±ìŠ¤ ì—…ë°ì´íŠ¸
            self.storageQueue.async(flags: .barrier) {
                self.diskCacheIndex[pageID] = pageDir.path
                self.memoryCache[pageID] = finalSnapshot  // ë©”ëª¨ë¦¬ ìºì‹œë„ ì—…ë°ì´íŠ¸
                self.dbg("ğŸ’¾ ë””ìŠ¤í¬ ì €ì¥ ì™„ë£Œ: \(snapshot.snapshot.pageRecord.title) [v\(version)]")
            }
            
            // 5. ì´ì „ ë²„ì „ ì •ë¦¬ (ìµœì‹  3ê°œë§Œ ìœ ì§€)
            self.cleanupOldVersions(pageID: pageID, tabID: tabID, currentVersion: version)
        }
    }
    
    private func cleanupOldVersions(pageID: UUID, tabID: UUID, currentVersion: Int) {
        let tabDir = tabDirectory(for: tabID)
        let pagePrefix = "Page_\(pageID.uuidString)_v"
        
        do {
            let contents = try FileManager.default.contentsOfDirectory(at: tabDir, includingPropertiesForKeys: nil)
            let pageDirs = contents.filter { $0.lastPathComponent.hasPrefix(pagePrefix) }
                .sorted { url1, url2 in
                    // ë²„ì „ ë²ˆí˜¸ ì¶”ì¶œí•˜ì—¬ ì •ë ¬
                    let v1 = Int(url1.lastPathComponent.replacingOccurrences(of: pagePrefix, with: "")) ?? 0
                    let v2 = Int(url2.lastPathComponent.replacingOccurrences(of: pagePrefix, with: "")) ?? 0
                    return v1 > v2  // ìµœì‹  ë²„ì „ë¶€í„°
                }
            
            // ìµœì‹  3ê°œ ì œì™¸í•˜ê³  ì‚­ì œ
            if pageDirs.count > 3 {
                for i in 3..<pageDirs.count {
                    try FileManager.default.removeItem(at: pageDirs[i])
                    dbg("ğŸ—‘ï¸ ì´ì „ ë²„ì „ ì‚­ì œ: \(pageDirs[i].lastPathComponent)")
                }
            }
        } catch {
            dbg("âš ï¸ ì´ì „ ë²„ì „ ì •ë¦¬ ì‹¤íŒ¨: \(error)")
        }
    }
    
